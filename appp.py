import streamlit as st
import pandas as pd
import plotly.express as px

# إعداد الصفحة
st.set_page_config(page_title="تحليل بيانات الأمن السيبراني", layout="wide")

# العنوان الرئيسي واسم الطالبة (لتلبية شرط التسليم)
st.title("لوحة تحكم تحليل بيانات الامتثال والمخاطر (GRC)")
st.subheader("تنفيذ المتدربة: سلمى البقمي")
st.write("---")

# 1. إعداد مجموعة البيانات (عينة تحاكي نتائج تدقيق أمني حقيقية للأقسام)
data = {
    'القسم': ['الموارد البشرية', 'المالية', 'تقنية المعلومات', 'التسويق', 'العمليات'],
    'نسبة الامتثال لضوابط (NCA)': [65, 85, 95, 55, 75],
    'عدد الحوادث الأمنية المسجلة': [12, 4, 1, 18, 8]
}
df = pd.DataFrame(data)

st.write("### نظرة عامة على البيانات")
st.dataframe(df)

# ==========================================
# الرسمة الأولى والقرار
# ==========================================
st.write("---")
st.write("### 1. مستوى الامتثال لضوابط الأمن السيبراني حسب القسم")

# رسم عمودي يوضح نسبة الامتثال
fig1 = px.bar(df, x='القسم', y='نسبة الامتثال لضوابط (NCA)', 
              color='القسم', text='نسبة الامتثال لضوابط (NCA)',
              title="مقارنة نسب الامتثال بين أقسام المنظمة")
fig1.update_traces(textposition='outside')
st.plotly_chart(fig1, use_container_width=True)

# استنتاج القرار (مظلل بلون مختلف لإبرازه)
st.warning("**القرار المستنتج:** إيقاف الصلاحيات المتقدمة مؤقتاً لموظفي قسم (التسويق) وإلزامهم ببرنامج توعوي عاجل، نظراً لتسجيلهم أقل نسبة امتثال (55%)، وذلك للحد من المخاطر التشغيلية وتجنب غرامات عدم الامتثال.")


# ==========================================
# الرسمة الثانية والقرار
# ==========================================
st.write("---")
st.write("### 2. توزيع الحوادث الأمنية المكتشفة على الأقسام")

# رسم دائري يوضح توزيع الحوادث
fig2 = px.pie(df, names='القسم', values='عدد الحوادث الأمنية المسجلة', 
              title="نسبة الحوادث الأمنية في كل قسم", hole=0.4)
st.plotly_chart(fig2, use_container_width=True)

# استنتاج القرار
st.success("**القرار المستنتج:** إعادة توجيه 40% من ميزانية التدريب التقني للربع القادم لصالح قسمي (التسويق والموارد البشرية) لتكثيف التدريب على اكتشاف هجمات التصيد، نظراً لاستحواذهما على النسبة الأكبر من الحوادث الأمنية.")